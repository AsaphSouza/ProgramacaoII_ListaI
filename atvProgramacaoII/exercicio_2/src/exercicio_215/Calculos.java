package exercicio_215;

public class Calculos {

	public int soma(int numero1, int numero2){
		int resultadoSoma = numero1 + numero2;
		return resultadoSoma;
	} 
	
	public int multiplicacao(int numero1, int numero2){
		int resultadoMultiplicacao = numero1 * numero2;
		return resultadoMultiplicacao;
	}
	
	public double divisao(double numero1, double numero2){
		double resultadoDivisao = numero1 / numero2;
		return resultadoDivisao;
	}
	
	public int subtracao(int numero1, int numero2){
		int resultadoSubtracao = numero1 - numero2;
		return resultadoSubtracao;
	}
}
